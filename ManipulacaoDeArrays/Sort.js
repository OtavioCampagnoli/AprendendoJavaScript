/*
    A função sort();
        Ordena os itens de um array;
*/

const frutas = [
    "Manga",
    "Laranja",
    "Maçã",
    "Banana",
    "Tangerina"
];

console.log("\nArray antes da ordenação:\n");
console.info(frutas);

frutas.sort();

console.log("\nArray depois da ordenação:\n");
console.info(frutas);
console.log("\n");  